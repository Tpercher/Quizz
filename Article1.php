	<h4> Avatar sortira pour Noël 2018, espère James Cameron! </h4>
		<p class="intro"> Après un premier film qui fut un chef-d'oeuvre dans le monde entier, "James Cameron" nous a récemment annoncé que le tournage de la suite allait bientôt commencer. Selon "James Cameron" : &ldquo;les suites seront spectaculaires.&rdquo; Cette longue attente d'une suite aura donc valu le coup. James Cameron compte transformer offrir à son film plusieurs suites à gros budget, mais il tarde à concrétiser ce rêve. <br />
		<strong> Où en est-il, exactement ? Et pourquoi cela prend autant de temps ? On vous dit tout! </strong>
		</p>
	<div class="main">
		<h3> Au moins 3 films au programme </h3>
			<p> Avant même la sortie du premier film, James Cameron évoquait déjà au moins la sortie de 3 films. En ce moment, James Cameron évoque même d'en faire 4, ce qui demande un travail colossal au niveau de l'écriture. Le producteur a d'ailleurs dit lors d'une interview : &laquo; C'est complexe de construire une histoire déclinée en trois films &#91;...&#93; Cela ne se prépare pas comme un seul opus. Là, on a trois scripts en cours d'écriture en même temps et il faut que ces trois récits soient cohérents entre eux. On ne veut pas s'attaquer à un long-métrage puis attendre qu'il soit sorti pour se demander ce qu'on fait ensuite. Et puis il y a tout l'univers à inventer. Tout le design. On doit créer de nouveaux environnements, de nouvelles créatures. C'est un long processus". &raquo; 
			</p>
		<h3> Un tournage colossal </h3>
			<p> &laquo; On ne les tournera pas à la suite, mais en même temps. Ce sera une énorme production, mise en scène comme une minisérie. On ne filmera pas l’intrigue dans l’ordre. <br />
			Par exemple, le lundi on pourra faire une séquence du quatrième film et le mardi une du premier. En tout, on devra raconter huit heures d’histoire, ce sera un challenge gigantesque, il ne faudra pas se perdre en cours de route. Ce sera le plus gros défi de ma carrière et je suis sûr que pour les comédiens aussi. Sur le plateau, ce sera : Non, non, non, non ce personnage n’est pas encore mort ! Tu es dans telle phase de l’action. &raquo; D'ailleurs lors d'une interview, l'interprète de Neytiri (Zoe Saldana) avait expliqué qu'il fallait au minimum 9 mois de travail intense pour que les acteurs soient prêts. On sait également de source sûr que le tournage aura lieu en Nouvelle-Zélande, le tournage est dont d'une ampleur hors normes et s'annonce impressionant.
			</p> 
		<h3> Sortie prévu entre 2018 et 2023 </h3>
			<p> Ca y est, James Cameron est lancé, les suites vont s'enchaîner. En effet, lors d'une interview, James Cameron a annoncé qu'Avatar 2 sortirait pour Noël 2018, Avatar 3 pour 2020, Avatar 4 pour 2022 et Avatar 5 pour 2023 mais aussi :&laquo;  On n’a pas encore changé de date, mais on le fera si besoin &#91;...&#93; Ce qui est important pour moi, ce n’est pas quand le prochain film sortira mais la cadence entre chaque film. Je veux qu’ils sortent de façon aussi rapprochée que possible. &raquo;
			</p>
